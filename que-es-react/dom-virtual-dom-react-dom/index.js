/* ¿Qué es el Virtual DOM?
Es una herramienta que implementa React para darle performance y velocidad a los desarrollos.
¿Qué significa? Que se tiene una copia fiel de lo que es el DOM (el Document Object Model del sitio).
Lo que esto permite, es comparar este Virtual DOM con el DOM, y chequear cuál es el que se actualizó
(el DOM necesita que se actualice para volver a cargar todos los cambios). */