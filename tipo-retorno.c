/**  Tipos de funciones por retorno.
Declaración: Firma de la función.
Definición: Lógica de la función.
	1) Sin argumentos y sin valor de retorno.
		void functionName();
	2) Funciones con argumentos, sin valor de retorno.
		void functionName(args);
	3) Funciones sin argumentos, pero sí valores a retornar.
		returnType functionName();
	4) Functiones con argumentos y return value.
		returnType functionName(args); */
